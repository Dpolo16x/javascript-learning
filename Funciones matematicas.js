/*
====================================================
OBJETO MATH EN JAVASCRIPT
====================================================

Math es un objeto que contiene constantes y funciones
matemáticas listas para usar.
*/


// =========================
// 🔹 CONSTANTES
// =========================

// PI (π) ≈ 3.14159
Math.PI;

// Número de Euler (e) ≈ 2.718
Math.E;

// Logaritmo natural de 2 ≈ 0.693
Math.LN2;



// =========================
// 🔹 REDONDEO
// =========================

// ceil() → redondea hacia arriba
Math.ceil(4.2); // 5

// floor() → redondea hacia abajo
Math.floor(4.8); // 4

// round() → redondea al más cercano
Math.round(4.5); // 5

// trunc() → elimina decimales
Math.trunc(4.9); // 4



// =========================
// 🔹 POTENCIAS Y RAÍCES
// =========================

// pow(a, b) → a elevado a b
Math.pow(2, 3); // 8

// sqrt() → raíz cuadrada
Math.sqrt(16); // 4

// cbrt() → raíz cúbica
Math.cbrt(8); // 2



// =========================
//  VALOR ABSOLUTO
// =========================

// abs() → elimina signo negativo
Math.abs(-10); // 10



// =========================
//  MÍNIMO Y MÁXIMO
// =========================

// min() → valor más pequeño
Math.min(9, 8, 7); // 7

// max() → valor más grande
Math.max(9, 8, 7); // 9



// =========================
//  LOGARITMOS
// =========================

// log() → logaritmo natural (base e)
Math.log(10);

// log2() → base 2
Math.log2(8); // 3

// log10() → base 10
Math.log10(100); // 2



// =========================
//  TRIGONOMETRÍA
// =========================

// sin() → seno
Math.sin(0);

// cos() → coseno
Math.cos(0);

// tan() → tangente
Math.tan(0);